#include <iostream>

using namespace std;

int main()
{
    int i = 5;
    float j = 2.5;
    double k = 2.3;

    cout << 2.3 + i + j*k << endl;
    cout << 10 + i + j*k << endl;
    cout << 2.3 + i << endl;
    cout << 10 + i << endl;
    cout << i + j*k << endl;
    cout << j * k << endl;
    cout << 10 + j * k << endl;
    return 0;
}
